public class rahul{
    private String domain;
    private String ipAddress;
    
    public rahul(String domain, String ipAddress){
        this.domain=domain;
        this.ipAddress=ipAddress;
    }
    
    public String getDomain(){
        return domain;
    }
    
    public String getIpAddress(){
        return ipAddress;
    }
}

